// Arithmetic operator
// +  _  *  /  %  ++  -- 

// incremental operator ++ , --
var a = 10
console.log(a++) // a = a + 1
console.log(++a) // a = a - 1

// preincremental/post incremental operator ++ , --
var b = 10
console.log(++b)
console.log(--b)

// short form
var a = 10
var b = 20
a += b // a = a + b
console.log(b)
a -= b
console.log(a)
a *= b
console.log(a)
a /= b
console.log(a)

