var a = new Date()
var b = a.getDay()
switch (b) {
  case 1:
    console.log('today is monday')
    break
  case 2:
    console.log('today is tuwesday')
    break
  case 3:
    console.log('today is wednesday')
    break
  case 4:
    console.log('today is thurstday')
    break
  case 5:
    console.log('today is friday')
    break
  case 6:
    console.log('today is saturday')
    break
  case 7:
    console.log('today is sunday')
    break
    default: console.log('something was wrong !')
}